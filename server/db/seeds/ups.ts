// ─── Build exports ────────────────────────────────────────────────────────────

function buildDocSteps(): StepRow[] {
  const rows: StepRow[] = [];
  for (const [wStr, prices] of Object.entries(UPS_DOC)) {
    const weight_kg = parseFloat(wStr);
    ZONES.forEach((zone, i) => {
      rows.push({ carrier_id: 'ups', zone_code: zone, shipment_type: 'document', weight_kg, price_inr: prices[i] });
    });
  }
  return rows;
}

function buildPkgSteps(): StepRow[] {
  const rows: StepRow[] = [];
  for (const [wStr, prices] of Object.entries(UPS_PKG)) {
    const weight_kg = parseFloat(wStr);
    ZONES.forEach((zone, i) => {
      rows.push({ carrier_id: 'ups', zone_code: zone, shipment_type: 'package', weight_kg, price_inr: prices[i] });
    });
  }
  return rows;
}

function buildBands(): BandRow[] {
  const rows: BandRow[] = [];
  for (const [wMin, wMax, perKgRates] of UPS_BAND_RATES) {
    ZONES.forEach((zone, i) => {
      rows.push({
        carrier_id: 'ups',
        zone_code: zone,
        shipment_type: 'package',
        weight_min_kg: wMin,
        weight_max_kg: wMax,
        price_per_kg: perKgRates[i],
        base_price_inr: 0,  // multiplicative: no base additive
        band_type: 'multiplicative',
      });
    });
  }
  return rows;
}

export const UPS_STEPS: StepRow[] = [...buildDocSteps(), ...buildPkgSteps()];
export const UPS_BANDS: BandRow[] = buildBands();
