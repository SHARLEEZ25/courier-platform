// ─── Build step rows ──────────────────────────────────────────────────────────
function buildRows(
  data: Record<number, number[]>,
  zones: string[],
  type: 'document' | 'package',
): StepRow[] {
  const rows: StepRow[] = [];
  for (const [wStr, prices] of Object.entries(data)) {
    const weight_kg = parseFloat(wStr);
    zones.forEach((zone, i) => {
      rows.push({ carrier_id: 'fedex', zone_code: zone, shipment_type: type, weight_kg, price_inr: prices[i] });
    });
  }
  return rows;
}

